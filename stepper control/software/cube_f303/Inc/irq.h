#if !defined (IRQ_H)
#define IRQ_H

#if defined (__ICCARM__)
#include   // for __get_interrupt_state() etc. (IAR)
#endif

#if defined (__GNUC__)

#define __get_interrupt_state __get_PRIMASK
#define __set_interrupt_state __set_PRIMASK

#endif // __GNUC__

static inline uint32_t disable_interrupts(void)
{
  uint32_t prior_state = __get_interrupt_state ();

  __disable_irq ();

  return prior_state;
}

static inline void restore_interrupts(uint32_t prior_state)
{
  __set_interrupt_state (prior_state);
}

#endif /* IRQ_H */
